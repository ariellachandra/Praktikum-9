    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        String hasil = labelHari.getText();
        
        if (!hasil.isEmpty()) {
            try (FileWriter writer = new FileWriter("C:/Users/lenovo/Desktop/hasil_perhitungan.txt")) {
                writer.write(hasil);
                JOptionPane.showMessageDialog(this, "Hasil perhitungan disimpan ke hasil_perhitungan.txt di Desktop.");
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat menyimpan file.");
                ex.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Tidak ada hasil untuk disimpan.");
        }        // TODO add your handling code here:
    }//GEN-LAST:event_btnSimpanActionPerformed

