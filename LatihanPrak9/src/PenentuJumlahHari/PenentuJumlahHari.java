    private void btnHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungActionPerformed
        int tahun = Integer.parseInt(teksTahun.getText());
        int bulan = pilihBulan.getSelectedIndex() + 1; 
        int jumlahHari = hitungHari.hitung(tahun, bulan);
        String namaBulan = (String) pilihBulan.getSelectedItem();
        labelHari.setText("Jumlah hari pada bulan " + namaBulan + " tahun " + tahun + " adalah " + jumlahHari);          // TODO add your handling code here:
    }//GEN-LAST:event_btnHitungActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        teksTahun.setText("");
        pilihBulan.setSelectedIndex(0);  
        labelHari.setText("");
        teksTahun.requestFocus();        
    }//GEN-LAST:event_btnHapusActionPerformed

